public class DivisionDifference {
    public static void main(String[] args) {
        int intA = 10;
        int intB = 3;
        int intResult = intA / intB;
        System.out.println("Integer division result (10 / 3): " + intResult);
        double doubleResult = (double) intA / intB;
        System.out.println("Double division result (10.0 / 3): " + doubleResult);
    }
}

